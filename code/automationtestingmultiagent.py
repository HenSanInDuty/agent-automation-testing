# STEP 2: CHUNKING
# ─────────────────────────────────────────────

def chunk_document(text: str, chunk_size: int = 1500, overlap: int = 200) -> list[str]:
    """
    Split document into overlapping chunks.
    Tries to split on section boundaries (##, Heading) first.
    Falls back to character-level sliding window.
    """
    # Try semantic splitting by section headers
    section_pattern = re.compile(
        r"(?=(?:#{1,3}\s|\n[A-Z][^\n]{0,80}\n[-=]{3,}|\n\d+\.\s+[A-Z]))",
        re.MULTILINE
    )
    sections = section_pattern.split(text)
    sections = [s.strip() for s in sections if s.strip()]

    chunks = []
    buffer = ""

    for section in sections:
        if len(buffer) + len(section) <= chunk_size:
            buffer += "\n\n" + section
        else:
            if buffer:
                chunks.append(buffer.strip())
            # If single section is too large, do sliding window
            if len(section) > chunk_size:
                for i in range(0, len(section), chunk_size - overlap):
                    chunks.append(section[i : i + chunk_size].strip())
            else:
                buffer = section

    if buffer.strip():
        chunks.append(buffer.strip())

    return chunks if chunks else [text]


