# MAIN PIPELINE
# ─────────────────────────────────────────────

def run_pipeline(
    file_path: str,
    output_path: Optional[str] = None,
    chunk_size: int = 1500,
    overlap: int = 200,
    client: Optional[LLMClient] = None,
) -> list[dict]:
    """
    Full pipeline: document -> structured test requirements.

    Args:
        file_path:   Path to input document (.txt, .md, .pdf, .docx)
        output_path: Optional path to save JSON output
        chunk_size:  Characters per chunk (default 1500)
        overlap:     Overlap between chunks (default 200)
        client:      LLMClient instance. Defaults to AnthropicClient (claude-sonnet-4-6)

    Returns:
        List of structured requirement dicts
    """
    client = client or AnthropicClient()

    print(f"📄 Parsing document: {file_path}")
    text = parse_document(file_path)
    print(f"   Extracted {len(text):,} characters")

    print(f"✂️  Chunking document...")
    chunks = chunk_document(text, chunk_size=chunk_size, overlap=overlap)
    print(f"   Created {len(chunks)} chunks")

    print(f"🧠 Analyzing chunks with Claude...")
    all_requirements = []
    for i, chunk in enumerate(chunks):
        print(f"   Chunk {i+1}/{len(chunks)}...", end=" ")
        reqs = analyze_chunk(client, chunk, chunk_index=i)
        print(f"found {len(reqs)} requirement(s)")
        all_requirements.extend(reqs)

    print(f"🔧 Post-processing...")
    all_requirements = deduplicate_requirements(all_requirements)
    all_requirements = clean_requirements(all_requirements)

    print(f"✅ Done! Extracted {len(all_requirements)} requirements total.")

    if output_path:
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(all_requirements, f, indent=2, ensure_ascii=False)
        print(f"💾 Saved to: {output_path}")

    return all_requirements

