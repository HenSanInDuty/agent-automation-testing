SYSTEM_PROMPT = """You are a senior QA engineer. Your job is to extract test requirements from documentation.

For each requirement or feature described, output a JSON object with this exact structure:
{
  "requirement_id": "REQ-<MODULE>-<NUMBER>",  // e.g. REQ-LOGIN-01
  "type": "API" or "UI",                       // API if involves endpoints/backend, UI if involves user interface
  "description": "short description under 100 words",
  "url": "url of route"
  "rules": [
    "validation rule 1",
    "validation rule 2"
  ]
}

Return a JSON array of all requirements found. If no clear requirements exist, return [].
Output ONLY valid JSON, no markdown, no explanation."""

def analyze_chunk(client: LLMClient, chunk: str, chunk_index: int) -> list[dict]:
    """Send a chunk to any LLMClient and extract structured requirements."""
    try:
        raw = client.complete(
            system=SYSTEM_PROMPT,
            user=f"Extract test requirements from this documentation chunk:\n\n{chunk}",
        )
        # Strip markdown code fences if present
        raw = re.sub(r"^```(?:json)?\s*|\s*```$", "", raw, flags=re.MULTILINE).strip()

        requirements = json.loads(raw)
        if isinstance(requirements, dict):
            requirements = [requirements]

        # Tag each requirement with source chunk
        for req in requirements:
            req["_source_chunk"] = chunk_index

        return requirements

    except (json.JSONDecodeError, KeyError) as e:
        print(f"  ⚠️  Chunk {chunk_index}: Failed to parse response — {e}")
        return []


def deduplicate_requirements(requirements: list[dict]) -> list[dict]:
    """Remove duplicate requirement IDs, renumber if needed."""
    seen_ids = {}
    result = []

    for req in requirements:
        req_id = req.get("requirement_id", "REQ-UNKNOWN-00")
        if req_id not in seen_ids:
            seen_ids[req_id] = True
            result.append(req)
        else:
            # Renumber duplicate
            base = re.sub(r"-\d+$", "", req_id)
            counter = 2
            while f"{base}-{counter:02d}" in seen_ids:
                counter += 1
            new_id = f"{base}-{counter:02d}"
            seen_ids[new_id] = True
            req["requirement_id"] = new_id
            result.append(req)

    return result


def clean_requirements(requirements: list[dict]) -> list[dict]:
    """Remove internal metadata fields before final output."""
    cleaned = []
    for req in requirements:
        cleaned.append({k: v for k, v in req.items() if not k.startswith("_")})
    return cleaned


