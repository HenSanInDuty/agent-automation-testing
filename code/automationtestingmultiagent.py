import os
import json
import re
from abc import ABC, abstractmethod
from pathlib import Path
from typing import Optional


# ─────────────────────────────────────────────
# LLM CLIENT ABSTRACTION
# ─────────────────────────────────────────────

class LLMClient(ABC):
    """Abstract base — implement this to plug in any LLM backend."""

    @abstractmethod
    def complete(self, system: str, user: str) -> str:
        """Send system + user prompt, return raw text response."""
        ...


class AnthropicClient(LLMClient):
    """Claude via Anthropic API."""

    def __init__(self, model: str = "claude-sonnet-4-6", api_key: Optional[str] = None):
        import anthropic
        self.model = model
        self.client = anthropic.Anthropic(api_key=api_key or os.environ.get("ANTHROPIC_API_KEY"))

    def complete(self, system: str, user: str) -> str:
        response = self.client.messages.create(
            model=self.model,
            max_tokens=2048,
            system=system,
            messages=[{"role": "user", "content": user}],
        )
        return response.content[0].text.strip()


class OpenAIClient(LLMClient):
    """OpenAI-compatible API (OpenAI, Azure, Together, Groq, vLLM, LM Studio…)."""

    def __init__(
        self,
        model: str = "gpt-4o",
        api_key: Optional[str] = None,
        base_url: Optional[str] = None,  # override for local/custom endpoints
    ):
        from openai import OpenAI
        self.model = model
        self.client = OpenAI(
            api_key=api_key or os.environ.get("OPENAI_API_KEY", "no-key"),
            base_url=base_url,  # e.g. "http://localhost:11434/v1" for Ollama
        )

    def complete(self, system: str, user: str) -> str:
        response = self.client.chat.completions.create(
            model=self.model,
            max_tokens=2048,
            messages=[
                {"role": "system", "content": system},
                {"role": "user", "content": user},
            ],
        )
        return response.choices[0].message.content.strip()


class OllamaClient(OpenAIClient):
    """Ollama local models — thin wrapper over OpenAIClient with preset base_url."""

    def __init__(self, model: str = "llama3", host: str = "http://localhost:11434"):
        super().__init__(model=model, api_key="ollama", base_url=f"{host}/v1")


class HuggingFaceClient(LLMClient):
    """
    HuggingFace Inference — local pipeline hoặc Inference API.

    Local (chạy model trên máy):
        client = HuggingFaceClient(model="ibm-granite/granite-3.0-2b-instruct")

    HF Inference API (chạy trên cloud HF, cần token):
        client = HuggingFaceClient(
            model="ibm-granite/granite-3.0-2b-instruct",
            use_api=True,
            api_key="hf_...",
        )

    Yêu cầu:
        pip install transformers torch          # local
        pip install huggingface_hub             # API
    """

    def __init__(
        self,
        model: str = "ibm-granite/granite-3.0-2b-instruct",
        api_key: Optional[str] = None,
        use_api: bool = False,
        max_new_tokens: int = 2048,
        device: Optional[str] = None,  # "cpu" | "cuda" | "mps" | None = auto
    ):
        self.model_id = model
        self.use_api = use_api
        self.api_key = api_key or os.environ.get("HF_TOKEN")
        self.max_new_tokens = max_new_tokens
        self.device = device
        self._pipeline = None   # lazy-load khi gọi complete() lần đầu

    def _get_pipeline(self):
        """Lazy-load local transformers pipeline."""
        if self._pipeline is not None:
            return self._pipeline

        try:
            import torch
            from transformers import pipeline, AutoTokenizer
        except ImportError:
            raise ImportError("Install: pip install transformers torch")

        # Auto-detect device nếu không truyền vào
        if self.device:
            device = self.device
        elif torch.cuda.is_available():
            device = "cuda"
        elif torch.backends.mps.is_available():
            device = "mps"
        else:
            device = "cpu"

        print(f"   🤗 Loading {self.model_id} on {device}...")
        tokenizer = AutoTokenizer.from_pretrained(self.model_id)
        self._pipeline = pipeline(
            "text-generation",
            model=self.model_id,
            tokenizer=tokenizer,
            device=device,
            torch_dtype=torch.float16 if device != "cpu" else torch.float32,
        )
        return self._pipeline

    def _format_prompt(self, system: str, user: str) -> str:
        """
        Granite 3.0 dùng chat template chuẩn.
        Dùng tokenizer.apply_chat_template nếu có, fallback sang string format.
        """
        try:
            from transformers import AutoTokenizer
            tokenizer = AutoTokenizer.from_pretrained(self.model_id)
            messages = [
                {"role": "system", "content": system},
                {"role": "user", "content": user},
            ]
            # add_generation_prompt=True để model biết phải generate tiếp
            return tokenizer.apply_chat_template(
                messages,
                tokenize=False,
                add_generation_prompt=True,
            )
        except Exception:
            # Fallback nếu tokenizer không có chat template
            return f"<|system|>\n{system}\n<|user|>\n{user}\n<|assistant|>\n"

    def complete(self, system: str, user: str) -> str:
        if self.use_api:
            return self._complete_via_api(system, user)
        return self._complete_local(system, user)

    def _complete_local(self, system: str, user: str) -> str:
        pipe = self._get_pipeline()
        prompt = self._format_prompt(system, user)

        outputs = pipe(
            prompt,
            max_new_tokens=self.max_new_tokens,
            do_sample=False,            # greedy — output ổn định hơn cho JSON
            return_full_text=False,     # chỉ trả phần generated, không lặp lại prompt
        )
        return outputs[0]["generated_text"].strip()

    def _complete_via_api(self, system: str, user: str) -> str:
        try:
            from huggingface_hub import InferenceClient
        except ImportError:
            raise ImportError("Install: pip install huggingface_hub")

        hf_client = InferenceClient(model=self.model_id, token=self.api_key)
        messages = [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ]
        response = hf_client.chat_completion(
            messages=messages,
            max_tokens=self.max_new_tokens,
        )
        return response.choices[0].message.content.strip()


